/*
ЗАДАЧА 1:
Дан массив, содержащий неотрицательные целые числа и строки.
Нужно вернуть новый массив, в котором будут только числа (строки удалить).

ЛОГИКА РЕШЕНИЯ:
1. У нас есть массив → нужен перебор
2. Нужно оставить элементы только одного типа
3. Проверяем тип элемента через typeof
4. Используем filter, чтобы отобрать только числа
*/

function filter_list(list) {
	return list.filter(item => typeof item === 'number'); // перебирает массив по одному элементу
}

console.log(filter_list([1, 2, 'a', 'b'])); // [1, 2]

// filter перебирает:
// item = 1   → true → оставить
// item = 2   → true → оставить
// item = 'a' → false → выбросить
// item = 'b' → false → выбросить

console.log(filter_list([1, 'a', 0, 15])); // 1, 0, 15]

/*
ЗАДАЧА 2:
Дана строка, состоящая из слов, разделённых пробелами.
Нужно вернуть длину самого короткого слова.

ЛОГИКА РЕШЕНИЯ:
1. Строку нужно разбить на слова (split)
2. У каждого слова есть длина
3. Берём длину первого слова как минимальную
4. Перебираем остальные слова и ищем меньшую длину
*/

function findShort(str) {
	const worlds = str.split(' '); // строка → массив слов
	let minLength = worlds[0].length; // минимум должен быть реальным значением
	// minLength - Какое самое короткое слово я видел ДО СИХ ПОР

	for (const word of worlds) {
		if (word.length < minLength) {
			minLength = word.length; // если нашли что-то меньше — обновляем

			// «Возьми массив worlds и по очереди подставляй каждый элемент в переменную word»
			// word = "bitcoin"
			// word = "take"
			// word = "over"
			// word = "the"
			// word = "world"
		}
	}
	return minLength;
}

console.log(findShort('bitcoin take over the world')); // 3

/*
ЗАДАЧА 3:
Дан массив чисел.
Нужно вернуть сумму всех положительных чисел.
Если положительных чисел нет — вернуть 0.

ЛОГИКА РЕШЕНИЯ:
1. Создаём переменную для суммы
2. Перебираем массив
3. Если число больше 0 — добавляем его к сумме
4. Возвращаем итоговую сумму
*/

function positiveSum(arr) {
	let sum = 0;

	for (const num of arr) {
		if (num > 0) {
			sum += num;
		}
	}

	return sum;
}

console.log(positiveSum([1, -4, 7, 12])); // 20

/*
ЗАДАЧА 4:
Дано целое число n (от 1 до 50).
Нужно вернуть строку, представляющую квадрат n × n,
состоящий из символов '+'.
Каждая строка отделяется символом переноса строки \n.

ПРИМЕР:
n = 3

+++
+++
+++

ЛОГИКА РЕШЕНИЯ:
1. Создаём строку из n символов '+'
2. Повторяем эту строку n раз
3. Соединяем строки через \n
*/

function generateShape(n) {
	const line = '+'.repeat(n);
	const result = [];

	for (let i = 0; i < n; i++) {
		result.push(line);
	}

	return result.join('\n');
}

/*
ЗАДАЧА 5:
Дана строка, состоящая из строчных букв и пробелов.
Нужно посчитать количество гласных букв.
Гласные: a, e, i, o, u (буква y не считается).

ЛОГИКА РЕШЕНИЯ:
1. Создаём строку с гласными
2. Перебираем каждый символ строки
3. Если символ есть среди гласных — увеличиваем счётчик
4. Возвращаем количество гласных
*/

function getCount(str) {
	const vowels = 'aeiou';
	let count = 0;

	for (const char of str) {
		if (vowels.includes(char)) {
			count++;
		}
	}

	return count;
}

console.log(getCount('abracadabra')); // 5
