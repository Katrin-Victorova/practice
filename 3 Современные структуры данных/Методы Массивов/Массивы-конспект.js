// Ментальная схема

/**
Группа 1: Добавление/удаление → всегда мутируют
push, pop, shift, unshift, splice
Запомнить легко: если физически “вырвали или вставили” — значит меняется массив.
 */

/**
Группа 2: “Копирование” → всегда НЕ мутируют
slice, concat, map, filter, flat
Копирование = новая коробка → оригинал не трогаем.
 */

/**
Группа 3: “Перестановка” → мутируют
sort, reverse, fill, copyWithin
Переставляем вещи внутри → меняется коробка → мутирует.
 */

// мини-пример
const arr = [1, 2, 3];

// Мутирующие:
arr.push(4); // arr: [1,2,3,4]
arr.pop(); // arr: [1,2,3]
arr.reverse(); // arr: [3,2,1]

// Немутирующие:
arr.slice(1); // [2,1] — новый массив
arr.map(n => n * 2); // [6,4,2]

/**
Каждый метод (filter, map, find, reduce) — это обычная функция, которая:
Перебирает элементы
На каждом элементе вызывает вашу функцию (callback)
В зависимости от результата callback что-то делает
(например, добавляет в результат или нет)
ВСЁ.

Колбэк — это функция, которую ты даёшь другому коду, а он будет вызывать её позже.
Аналогия:
Ты нанимаешь помощника и говоришь:
«Вот список дел. На каждый пункт — позвони мне и скажи, ок он или нет».
Помощник = метод массива
Ты = callback
 */
