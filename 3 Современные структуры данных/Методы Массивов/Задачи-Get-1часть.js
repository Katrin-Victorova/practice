/**
 * –ó–∞–¥–∞—á–∞ 1 ‚Äî filter: –æ—Å—Ç–∞–≤–∏—Ç—å —Ç–æ–ª—å–∫–æ —á—ë—Ç–Ω—ã–µ —á–∏—Å–ª–∞
 * –î–∞–Ω –º–∞—Å—Å–∏–≤ —á–∏—Å–µ–ª. –ò—Å–ø–æ–ª—å–∑—É—è –º–µ—Ç–æ–¥ filter, —Å–æ–∑–¥–∞–π—Ç–µ –Ω–æ–≤—ã–π –º–∞—Å—Å–∏–≤ —Ç–æ–ª—å–∫–æ —Å —á—ë—Ç–Ω—ã–º–∏ —á–∏—Å–ª–∞–º–∏. */

const numbers = [1, 2, 3, 4, 5, 6];

// 1. –≤—ã–∑—ã–≤–∞–µ–º —É –º–∞—Å—Å–∏–≤–∞ –º–µ—Ç–æ–¥ filter
// 2. –≤ filter –ø–µ—Ä–µ–¥–∞—ë–º –∫–æ–ª–±—ç–∫-—Ñ—É–Ω–∫—Ü–∏—é
// 3. –∫–æ–ª–±—ç–∫ –ø–æ–ª—É—á–∞–µ—Ç –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç –º–∞—Å—Å–∏–≤–∞ (num)
// 4. –µ—Å–ª–∏ –≤–µ—Ä–Ω—ë–º true ‚Üí —ç—Ç–æ—Ç —ç–ª–µ–º–µ–Ω—Ç –ø–æ–ø–∞–¥—ë—Ç –≤ –Ω–æ–≤—ã–π –º–∞—Å—Å–∏–≤
// 5. —É—Å–ª–æ–≤–∏–µ "—á–∏—Å–ª–æ —á—ë—Ç–Ω–æ–µ" ‚Üí num % 2 === 0

const evenNumbers = numbers.filter(function (num) {
	return num % 2 === 0; // —á—ë—Ç–Ω—ã–µ —á–∏—Å–ª–∞ ‚Üí –æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è –Ω–∞ 2 —Ä–∞–≤–µ–Ω 0
});

console.log(evenNumbers); // [2, 4, 6]

/**
 * –ó–∞–¥–∞—á–∞ 2 ‚Äî map: —Å–¥–µ–ª–∞—Ç—å –í–°–ï —Å—Ç—Ä–æ–∫–∏ –≤–µ—Ä—Ö–Ω–∏–º —Ä–µ–≥–∏—Å—Ç—Ä–æ–º
 * –ò—Å–ø–æ–ª—å–∑—É—è –º–µ—Ç–æ–¥ map, —Å–æ–∑–¥–∞–π—Ç–µ –Ω–æ–≤—ã–π –º–∞—Å—Å–∏–≤, –≥–¥–µ –≤—Å–µ —Å—Ç—Ä–æ–∫–∏ –±—É–¥—É—Ç –≤ –≤–µ—Ä—Ö–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ. */

const fruits = ['apple', 'banana', 'cherry'];

// 1. map –≤—Å–µ–≥–¥–∞ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –ù–û–í–´–ô –º–∞—Å—Å–∏–≤ —Ç–∞–∫–æ–π –∂–µ –¥–ª–∏–Ω—ã
// 2. –∫–æ–ª–±—ç–∫ –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç (fruit)
// 3. —á—Ç–æ –º—ã –≤–µ—Ä–Ω—ë–º –∏–∑ –∫–æ–ª–±—ç–∫–∞ ‚Üí —Ç–æ –∏ –æ–∫–∞–∂–µ—Ç—Å—è –≤ –Ω–æ–≤–æ–º –º–∞—Å—Å–∏–≤–µ
// 4. –º–µ—Ç–æ–¥ —Å—Ç—Ä–æ–∫–∏ toUpperCase() –¥–µ–ª–∞–µ—Ç –í–°–ï –±—É–∫–≤—ã –∑–∞–≥–ª–∞–≤–Ω—ã–º–∏

const upperFruits = fruits.map(function (fruit) {
	return fruit.toUpperCase();
});

console.log(upperFruits); // ["APPLE", "BANANA", "CHERRY"]

/**
 * –ó–∞–¥–∞—á–∞ 3 ‚Äî some: –µ—Å—Ç—å –ª–∏ —Ö–æ—Ç—è –±—ã –æ–¥–Ω–æ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ–µ —á–∏—Å–ª–æ
 * –ò—Å–ø–æ–ª—å–∑—É—è –º–µ—Ç–æ–¥ some, –ø—Ä–æ–≤–µ—Ä—å—Ç–µ, –µ—Å—Ç—å –ª–∏ –≤ –º–∞—Å—Å–∏–≤–µ —Ö–æ—Ç—è –±—ã –æ–¥–Ω–æ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ–µ —á–∏—Å–ª–æ. */

const nums = [3, -1, 4, 0];

// 1. some –ø—Ä–æ–≤–µ—Ä—è–µ—Ç: "–µ—Å—Ç—å –ª–∏ —Ö–æ—Ç—è –±—ã –û–î–ò–ù —ç–ª–µ–º–µ–Ω—Ç, –¥–ª—è –∫–æ—Ç–æ—Ä–æ–≥–æ –∫–æ–ª–±—ç–∫ –≤–µ—Ä–Ω—ë—Ç true"
// 2. kol–±—ç–∫ –ø–æ–ª—É—á–∞–µ—Ç —ç–ª–µ–º–µ–Ω—Ç (num)
// 3. —É—Å–ª–æ–≤–∏–µ "—á–∏—Å–ª–æ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ–µ" ‚Üí num < 0
// 4. –µ—Å–ª–∏ –Ω–∞–π–¥—ë—Ç—Å—è —Ö–æ—Ç—è –±—ã –æ–¥–∏–Ω —Ç–∞–∫–æ–π —ç–ª–µ–º–µ–Ω—Ç ‚Üí some –≤–µ—Ä–Ω—ë—Ç true

const hasNegative = nums.some(function (num) {
	return num < 0;
});

console.log(hasNegative); // true

/**
 * –ó–∞–¥–∞—á–∞ 4 ‚Äî every: –≤—Å–µ –ª–∏ —á–∏—Å–ª–∞ –±–æ–ª—å—à–µ –Ω—É–ª—è
 * –ò—Å–ø–æ–ª—å–∑—É—è –º–µ—Ç–æ–¥ every, –ø—Ä–æ–≤–µ—Ä—å—Ç–µ, —á—Ç–æ –≤—Å–µ —á–∏—Å–ª–∞ –≤ –º–∞—Å—Å–∏–≤–µ –±–æ–ª—å—à–µ –Ω—É–ª—è. */

const numSum = [3, 1, 4, 0];

// 1. every –ø—Ä–æ–≤–µ—Ä—è–µ—Ç: "–≤—Å–µ –ª–∏ —ç–ª–µ–º–µ–Ω—Ç—ã –ø–æ–¥—Ö–æ–¥—è—Ç –ø–æ–¥ —É—Å–ª–æ–≤–∏–µ?"
// 2. kol–±—ç–∫ –ø–æ–ª—É—á–∞–µ—Ç —ç–ª–µ–º–µ–Ω—Ç (num)
// 3. —É—Å–ª–æ–≤–∏–µ "—á–∏—Å–ª–æ –±–æ–ª—å—à–µ –Ω—É–ª—è" ‚Üí num > 0
// 4. –µ—Å–ª–∏ –•–û–¢–Ø –ë–´ –û–î–ò–ù —ç–ª–µ–º–µ–Ω—Ç –Ω–µ –ø–æ–¥—Ö–æ–¥–∏—Ç ‚Üí –≤–µ—Ä–Ω—ë—Ç false

const allPositive = numSum.every(function (num) {
	return num > 0;
});

console.log(allPositive); // false

/**
 * –ó–∞–¥–∞—á–∞ 5: –°—É–º–º–∞ —á–∏—Å–µ–ª –º–∞—Å—Å–∏–≤–∞ (–º–µ—Ç–æ–¥ reduce) */
const numbers1 = [1, 2, 3, 4, 5];

// reduce –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –∫–æ–ª–±—ç–∫ –∏ –Ω–∞—á–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –∞–∫–∫—É–º—É–ª—è—Ç–æ—Ä–∞ (0)
const sum = numbers1.reduce((acc, num) => {
	// acc ‚Äî —ç—Ç–æ —Å—É–º–º–∞ –Ω–∞ –ø—Ä–µ–¥—ã–¥—É—â–µ–º —à–∞–≥–µ
	// num ‚Äî —Ç–µ–∫—É—â–∏–π —ç–ª–µ–º–µ–Ω—Ç –º–∞—Å—Å–∏–≤–∞

	console.log(`acc: ${acc}, num: ${num}`); // –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –ø—Ä–æ—Ü–µ—Å—Å

	return acc + num; // –æ–±–Ω–æ–≤–ª—è–µ–º –∞–∫–∫—É–º—É–ª—è—Ç–æ—Ä
}, 0); // –Ω–∞—á–∞–ª—å–Ω–∞—è —Å—É–º–º–∞ = 0

console.log(sum); // 15

