// Задача 3: Декременты в цикле
let i = 3;
while (--i > 0) {
	console.log(i);
} // 2,1

/** 
 Разбор:
 Префиксный --i уменьшает i перед проверкой условия.
 i = 3 → --i → i = 2 → условие 2 > 0 → вывод 2
 --i → i = 1 → условие 1 > 0 → вывод 1
 --i → i = 0 → условие 0 > 0 → false, выход.
 → Ответ: 2, 1.
 */
