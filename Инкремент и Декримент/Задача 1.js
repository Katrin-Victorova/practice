// Задача 1:
let m = 2;
let n = m-- + ++m * m++;
console.log(n);

// m-- → возвращает 2, m = 1
// ++m → m = 2, возвращает 2
// m++ → возвращает 2, m = 3
// n = 2 + (2 * 2) → 6

// Задача 2:
let x = 1;
x = x++ + ++x + x;
console.log(x);

// x++ → возвращает 1, x = 2
// ++x → x = 3, возвращает 3
// Последний x → 3
// x = 1 + 3 + 3 → 7
