// Задача: Префикс + Постфикс + Декремент
let x = 4;
let y = ++x - x-- + x++;
console.log(y); // 4
console.log(x); // 5

/** 
 Разбор:
 ++x →
 x = 4 + 1 = 5
 Возвращает 5 (префикс).
 → Выражение пока: 5 - ... + ...
 x-- →
 Возвращает текущий x = 5 (постфикс).
 x = 5 - 1 = 4
 → Выражение: 5 - 5 + ...
 x++ →
 Возвращает текущий x = 4 (постфикс).
 x = 4 + 1 = 5
 → Выражение: 5 - 5 + 4
 Итог:
 y = 5 - 5 + 4 = 4
 → Ответ: 4.
 */
