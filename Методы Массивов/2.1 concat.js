// 2.1 concat()
// Создает новый массив, обьединяя исходный массов с другими массивами или значениями
// Принимаеи рлин иои несколько массивов / значений
const arr1 = [1, 2];
const arr2 = [3, 4];
const result = arr1.concat(arr2, 5) // [ 1, 2, 3, 4, 5 ]
// Возвращает новый массив
// Не изменяет исходный массив
// Удобен для обьединения массивов
// Для больших массивов может потребоваться больше памяти